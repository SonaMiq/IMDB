package com.sonmiq.demo.staff;

public class MovieStaff {
  private   String name;
  private   String surname;

    public MovieStaff(String name, String surname){
        this.name=name;
        this.surname=surname;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }
}
